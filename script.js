const questionNumberElement = document.getElementById('question-number');
const questionTextElement = document.getElementById('question-text');
const optionsContainer = document.getElementById('options-container');
const submitButton = document.getElementById('submit-button');
const feedbackArea = document.getElementById('feedback-area');
const nextButton = document.getElementById('next-button');
const resultArea = document.getElementById('result-area');
const scoreTextElement = document.getElementById('score-text');
const restartButton = document.getElementById('restart-button');
const wrongAnswersList = document.getElementById('wrong-answers-list');
const quizContainer = document.querySelector('.quiz-container'); // 全体コンテナ

let currentQuestionIndex = 0;
let score = 0;
let selectedOption = null;
let wrongAnswers = [];

// クイズのシャッフル（オプション）
// quizData.sort(() => Math.random() - 0.5);

function loadQuestion() {
    // スクロールをトップに戻す
    quizContainer.scrollTop = 0;

    const currentQuiz = quizData[currentQuestionIndex];
    questionNumberElement.textContent = `問題 ${currentQuestionIndex + 1} / ${quizData.length}`;
    questionTextElement.textContent = currentQuiz.question;

    optionsContainer.innerHTML = '';
    currentQuiz.options.forEach(option => {
        const button = document.createElement('button');
        button.classList.add('option-button');
        button.textContent = option;
        button.addEventListener('click', () => selectOption(button, option));
        optionsContainer.appendChild(button);
    });

    submitButton.disabled = true;
    submitButton.style.display = 'block';
    nextButton.style.display = 'none';
    feedbackArea.textContent = '';
    feedbackArea.className = ''; // クラスをリセット
    selectedOption = null;
}

function selectOption(button, option) {
    // 既に選択済みのボタンがあればスタイルをリセット
    const currentSelected = document.querySelector('.option-button.selected');
    if (currentSelected) {
        currentSelected.classList.remove('selected');
    }

    button.classList.add('selected');
    selectedOption = option;
    submitButton.disabled = false;
}

function checkAnswer() {
    if (!selectedOption) return;

    const currentQuiz = quizData[currentQuestionIndex];
    const optionButtons = optionsContainer.querySelectorAll('.option-button');

    // 全ての選択肢ボタンを無効化
    optionButtons.forEach(button => button.disabled = true);

    if (selectedOption === currentQuiz.correct_answer) {
        score++;
        feedbackArea.textContent = '✅ 正解です！';
        feedbackArea.classList.add('correct-feedback');
        // 正解のボタンを緑色に
        optionButtons.forEach(button => {
            if (button.textContent === currentQuiz.correct_answer) {
                button.classList.add('correct');
            }
        });
    } else {
        feedbackArea.innerHTML = `❌ 不正解です。<br>正解は「<strong>${currentQuiz.correct_answer}</strong>」でした。`;
        feedbackArea.classList.add('wrong-feedback');
        wrongAnswers.push({
            question: currentQuiz.question,
            correct_answer: currentQuiz.correct_answer,
            your_answer: selectedOption,
            explanation: currentQuiz.explanation
        });
        // 不正解のボタンを赤色に、正解のボタンを緑色に
        optionButtons.forEach(button => {
            if (button.textContent === selectedOption) {
                button.classList.add('wrong');
            } else if (button.textContent === currentQuiz.correct_answer) {
                button.classList.add('correct');
            }
        });
    }

    feedbackArea.innerHTML += `<p><strong>解説:</strong> ${currentQuiz.explanation}</p>`;
    submitButton.style.display = 'none';
    nextButton.style.display = 'block';
}

function nextQuestion() {
    currentQuestionIndex++;
    if (currentQuestionIndex < quizData.length) {
        loadQuestion();
    } else {
        showResult();
    }
}

function showResult() {
    document.getElementById('question-area').style.display = 'none';
    resultArea.style.display = 'block';
    scoreTextElement.textContent = `${quizData.length}問中 ${score}問正解！スコア: ${score}`;

    wrongAnswersList.innerHTML = '';
    if (wrongAnswers.length > 0) {
        wrongAnswers.forEach((item, index) => {
            const listItem = document.createElement('li');
            listItem.innerHTML = `
                <strong>問題 ${item.id || index + 1}:</strong> ${item.question}<br>
                あなたの回答: <strong>${item.your_answer}</strong><br>
                正解: <em>${item.correct_answer}</em><br>
                解説: ${item.explanation}
            `;
            wrongAnswersList.appendChild(listItem);
        });
    } else {
        wrongAnswersList.innerHTML = '<li>お見事！全問正解です！👏</li>';
    }
}

function restartQuiz() {
    currentQuestionIndex = 0;
    score = 0;
    wrongAnswers = [];
    resultArea.style.display = 'none';
    document.getElementById('question-area').style.display = 'block';
    // クイズデータをシャッフルし直す（任意）
    // quizData.sort(() => Math.random() - 0.5);
    loadQuestion();
}

// イベントリスナー
submitButton.addEventListener('click', checkAnswer);
nextButton.addEventListener('click', nextQuestion);
restartButton.addEventListener('click', restartQuiz);

// 初期ロード
loadQuestion();